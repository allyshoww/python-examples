variavel = '   python - exemplos    '

# Printa em forma de titulo
variavel.title()

#Printa com todoas as letras maisculas
variavel.upper()

#Printa com todas as letras minusculas
variavel.lower()

#Substitui o e por 3
variavel.replace('e','3')

#Elimina espaços vazios antes da string
variavel.strip()

#Quebra a variavel em uma lista
variavel.split()

# Verifica se todas as letras sao minusculas
variavel.islower()

#Verifica se a variavel começa com a letra c
variavel.startswith('c')

#Interage uma string com a outra
'.'.join(variavel)