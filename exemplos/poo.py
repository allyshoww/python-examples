# POO busca abstrair a programação para coisas do mundo real.
# Um software não é composto por um grande bloco de funcionalidades especificas, mas sim, por varios blocos com funcionalidades distintas e independentes...
# que juntos formam um sistema.

# Atende a 4 caracteristicas:
# Abstracao: Abstração de coisas do mundo real para o mundo de software
# Encapsulamento: Encapsula informações de background para rodar o sistema não mostrando para o usuário
# Herança: Herança de informações e comportamento da classe pai para a classe filho
# Polimorfismo: Objetos que herdam mesmas caracteristicas e propriedades da classe pai

#VANTAGENS#

# Reutilização do código
# Escalabilidade
# Manutenção agil
# Agilidade de desenvolvimento
